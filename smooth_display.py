try:
    from PyQt5.QtCore import QObject, pyqtSignal
except ImportError:
    from PyQt6.QtCore import QObject, pyqtSignal


class ThreadSafeSampleQueue(QObject):
    """
    Thread-safe bridge between a background serial thread and the UI thread.
    Use this if your data arrives on a non-UI thread.

    USAGE:
        # Setup (UI thread):
        self.queue = ThreadSafeSampleQueue()
        self.queue.samples_ready.connect(self.ecg_display.push_samples)

        # In background serial thread:
        self.queue.emit_samples({'I': [...], 'II': [...]})
    """
    samples_ready = pyqtSignal(dict)

    def emit_samples(self, lead_data: dict):
        """
        Emit new samples from background thread.
        Qt's signal/slot mechanism marshals this to the UI thread safely.
        """
        self.samples_ready.emit(lead_data)


