# ─────────────────────────────────────────────────────────────────────────────
# INCREMENTAL SIGNAL FILTER
# Filters samples AS THEY ARRIVE using lfilter + initial conditions (zi).
# No full-buffer recalculation on every render frame.
# ─────────────────────────────────────────────────────────────────────────────

class IncrementalFilter:
    """
    Applies a digital filter incrementally as new samples arrive.
    Uses scipy.signal.lfilter with maintained state (zi) so each call
    only processes NEW samples — O(n_new) not O(n_buffer).

    This replaces the anti-pattern of calling filtfilt() on the entire
    buffer each render frame.
    """

    def __init__(self, sampling_rate: float = 500.0,
                 lowcut: float = 0.5,
                 highcut: float = 40.0,
                 notch_freq: float = 50.0,
                 notch_q: float = 35.0,
                 order: int = 4,
                 adc_center: float = 2048.0):

        self.fs = sampling_rate
        self.adc_center = adc_center

        # ── Bandpass filter (0.5–40 Hz) ──────────────────────────────────────
        nyq = sampling_rate / 2.0
        low = max(0.001, lowcut / nyq)
        high = min(0.999, highcut / nyq)
        self.b_bp, self.a_bp = butter(order, [low, high], btype='band')

        # ── Notch filter (50 Hz or 60 Hz powerline) ──────────────────────────
        w0 = notch_freq / nyq
        if 0 < w0 < 1:
            self.b_notch, self.a_notch = iirnotch(w0, notch_q)
            self._notch_enabled = True
        else:
            self._notch_enabled = False

        # ── Initial conditions at ADC midpoint (prevents startup spike) ──────
        self.zi_bp = lfilter_zi(self.b_bp, self.a_bp) * adc_center
        if self._notch_enabled:
            self.zi_notch = lfilter_zi(self.b_notch, self.a_notch) * adc_center

    def process(self, samples: np.ndarray) -> np.ndarray:
        """
        Filter new samples and return filtered output.
        Maintains filter state between calls — no startup transients.

        Args:
            samples: 1-D numpy array of new ADC samples

        Returns:
            Filtered samples (same length as input)
        """
        if len(samples) == 0:
            return samples

        samples = np.asarray(samples, dtype=float)

        # Step 1: Notch filter (50/60 Hz removal)
        if self._notch_enabled:
            samples, self.zi_notch = lfilter(
                self.b_notch, self.a_notch, samples, zi=self.zi_notch
            )

        # Step 2: Bandpass (baseline wander + EMG removal)
        filtered, self.zi_bp = lfilter(
            self.b_bp, self.a_bp, samples, zi=self.zi_bp
        )

        return filtered

    def reset(self, value: float = None):
        """Reset filter state. Call when lead is reconnected."""
        v = value if value is not None else self.adc_center
        self.zi_bp = lfilter_zi(self.b_bp, self.a_bp) * v
        if self._notch_enabled:
            self.zi_notch = lfilter_zi(self.b_notch, self.a_notch) * v


# ─────────────────────────────────────────────────────────────────────────────
# SMOOTH LEAD BUFFER
# Fixed-size circular buffer with incremental filtering and baseline tracking.
# ─────────────────────────────────────────────────────────────────────────────

class SmoothLeadBuffer:
    """
    Per-lead data buffer that:
    - Accepts data at any arrival rate
    - Filters incrementally (no per-frame filtfilt)
    - Removes baseline wander with a rolling median baseline
    - Provides stable, fixed-length display arrays
    - Tracks write position for scrolling cursor display
    """

    def __init__(self,
                 sampling_rate: float = 500.0,
                 display_seconds: float = 5.0,
                 adc_center: float = 2048.0,
                 adc_scale: float = 204.8,
                 lead_name: str = '?'):

        self.fs = sampling_rate
        self.adc_center = adc_center
        self.adc_scale = adc_scale  # ADC counts per mV
        self.lead_name = lead_name
        self.display_samples = int(display_seconds * sampling_rate)

        # ── Main circular buffers ─────────────────────────────────────────────
        # Pre-fill with midpoint so display starts flat (not empty/garbage)
        self.raw_buffer = deque(
            [float(adc_center)] * self.display_samples,
            maxlen=self.display_samples
        )
        self.filtered_buffer = deque(
            [0.0] * self.display_samples,
            maxlen=self.display_samples
        )

        # ── Incremental filter ────────────────────────────────────────────────
        self.filt = IncrementalFilter(
            sampling_rate=sampling_rate,
            adc_center=adc_center
        )

        # ── Write position for scrolling cursor ───────────────────────────────
        self.write_pos = 0

        # ── Lead-off detection state ──────────────────────────────────────────
        self.is_lead_off = False
        self._check_window = deque(maxlen=int(sampling_rate * 1.0))  # 1s window

        # ── Baseline tracking (slow rolling median for display centering) ─────
        self._baseline_buffer = deque(maxlen=int(sampling_rate * 2.0))  # 2s
        self._display_baseline = 0.0

    def push(self, samples):
        """
        Push new samples from hardware packet.
        Called in data timer — does filtering and buffering.

        Args:
            samples: list or ndarray of raw ADC values
        """
        if len(samples) == 0:
            return

        arr = np.asarray(samples, dtype=float)

        # Lead-off detection on raw signal
        self._check_window.extend(arr.tolist())
        self.is_lead_off = self._detect_lead_off()

        # Store raw
        self.raw_buffer.extend(arr.tolist())

        if self.is_lead_off:
            # Fill with flat line at center (don't show noise)
            self.filtered_buffer.extend([0.0] * len(arr))
        else:
            # Incremental filter
            filtered = self.filt.process(arr)
            self.filtered_buffer.extend(filtered.tolist())
            self._baseline_buffer.extend(filtered.tolist())
            # Update rolling baseline (very slow — only for vertical centering)
            if len(self._baseline_buffer) > 50:
                self._display_baseline = float(np.median(
                    list(self._baseline_buffer)[-int(self.fs * 0.5):]
                ))

        # Advance write position
        self.write_pos = (self.write_pos + len(arr)) % self.display_samples

    def get_display_array(self) -> np.ndarray:
        """
        Returns correctly-ordered display array (oldest → newest left to right).
        Write position is always at the RIGHT edge.

        Returns:
            np.ndarray shape (display_samples,) in display units (mV centered at 0)
        """
        buf = list(self.filtered_buffer)
        pos = self.write_pos

        # Rotate so write_pos is at right edge
        ordered = buf[pos:] + buf[:pos]
        arr = np.array(ordered, dtype=float)

        # Subtract rolling baseline (removes any residual DC drift)
        arr = arr - self._display_baseline

        return arr

    def get_write_pos_display(self) -> int:
        """
        Returns the current write position in the display array.
        Used to draw the scrolling cursor eraser.
        Always returns self.display_samples - 1 (right edge) since
        get_display_array() rotates so write is always at the right.
        """
        return self.display_samples - 1

    def _detect_lead_off(self) -> bool:
        """Fast lead-off detection on 1-second window."""
        if len(self._check_window) < 50:
            return False
        w = np.array(self._check_window)
        amplitude = float(np.ptp(w))        # Peak-to-peak
        variance = float(np.var(w))

        if amplitude < 20:                   # < ~0.1 mV → flatline
            return True
        if amplitude > 3500:                 # Saturation
            return True
        if variance < 4:                     # True flatline
            return True
        if variance > 300000:                # Pure noise (lead fully off)
            return True
        min_v, max_v = float(np.min(w)), float(np.max(w))
        if min_v <= 5 or max_v >= 4090:     # ADC rail
            return True
        return False

    def reset(self):
        """Reset buffer (call on reconnect or settings change)."""
        self.raw_buffer = deque(
            [float(self.adc_center)] * self.display_samples,
            maxlen=self.display_samples
        )
        self.filtered_buffer = deque(
            [0.0] * self.display_samples,
            maxlen=self.display_samples
        )
        self.filt.reset(self.adc_center)
        self.write_pos = 0
        self._display_baseline = 0.0
        self.is_lead_off = False


# ─────────────────────────────────────────────────────────────────────────────
# SMOOTH LEAD RENDERER
# Wraps a single PyQtGraph PlotWidget for one ECG lead.
# Manages curves, cursor eraser, lead-off overlay, fixed axes.
# ─────────────────────────────────────────────────────────────────────────────

# ECG color scheme — tweak to match your UI
ECG_COLORS = {
    'waveform':    '#00FF41',   # Bright green (classic monitor)
    'cursor':      '#000000',   # Black eraser cursor
    'grid_major':  '#1A3A1A',   # Dark green grid
    'grid_minor':  '#0D1F0D',
    'background':  '#000000',   # Pure black
    'lead_off_bg': '#1A0000',   # Dark red tint when lead disconnected
    'lead_off_txt': '#FF3333',  # Red "LEAD OFF" text
    'label':       '#00AA33',   # Lead name label
}

# Cursor eraser width in samples
CURSOR_ERASE_SAMPLES = 40


class SmoothLeadRenderer:
    """
    Renders one ECG lead smoothly using a PyQtGraph PlotWidget.

    Features:
    - Fixed Y axis (no auto-range jumping)
    - Scrolling cursor eraser (no teleport at wrap-around)
    - Lead-off overlay with red background + "LEAD OFF" text
    - Grid with standard ECG 1mV / 200ms divisions
    - Minimal redraw overhead (clip-to-view, no antialias)
    """

    # Standard ECG display range in filtered units
    DISPLAY_MV_RANGE = 3.0      # ±3 mV display window
    ADC_SCALE = 204.8           # ADC counts per mV (12-bit, ±10V)

    def __init__(self,
                 plot_widget,
                 lead_name: str,
                 display_samples: int = 2500,
                 sampling_rate: float = 500.0):

        self.pw = plot_widget
        self.lead_name = lead_name
        self.display_samples = display_samples
        self.fs = sampling_rate
        self.x_axis = np.arange(display_samples, dtype=float)

        self._lead_off_shown = False

        # ── Configure plot widget ─────────────────────────────────────────────
        self.pw.setBackground(ECG_COLORS['background'])
        self.pw.setMenuEnabled(False)
        self.pw.hideButtons()
        self.pw.setMouseEnabled(x=False, y=False)
        self.pw.setDownsampling(auto=True, mode='peak')
        self.pw.setClipToView(True)

        # Disable all axis labels/ticks for clean look (optional — comment out if you want axes)
        for axis in ['left', 'bottom', 'top', 'right']:
            ax = self.pw.getAxis(axis)
            ax.setStyle(showValues=False, tickLength=0)
            ax.setPen(pg.mkPen(color='#1A3A1A', width=1))

        # ── Fixed axes — SET ONCE, NEVER CHANGE ──────────────────────────────
        # Y axis: filtered signal is centered at 0 (baseline-subtracted)
        # Units are raw ADC-scale counts after bandpass (not mV, not normalized)
        # ±3 mV * 204.8 counts/mV = ±614 counts display range
        y_range = self.DISPLAY_MV_RANGE * self.ADC_SCALE
        self.pw.setYRange(-y_range, y_range, padding=0)
        self.pw.setXRange(0, display_samples, padding=0)

        # CRITICAL: Lock auto-range OFF
        self.pw.enableAutoRange(axis='y', enable=False)
        self.pw.enableAutoRange(axis='x', enable=False)

        # ── ECG grid (1 mV vertical, 200ms horizontal) ────────────────────────
        self._draw_grid()

        # ── Lead name label (top-left) ────────────────────────────────────────
        self.label = pg.TextItem(
            text=lead_name,
            color=ECG_COLORS['label'],
            anchor=(0, 0)
        )
        self.label.setFont(pg.QtGui.QFont('Courier New', 9, pg.QtGui.QFont.Weight.Bold))
        self.pw.addItem(self.label)
        self.label.setPos(10, y_range * 0.85)

        # ── Main waveform curve ───────────────────────────────────────────────
        self.curve = pg.PlotDataItem(
            pen=pg.mkPen(color=ECG_COLORS['waveform'], width=1.5),
            skipFiniteCheck=True,
            dynamicRangeLimit=None,
        )
        self.pw.addItem(self.curve)

        # ── Cursor eraser (black band that sweeps ahead of write head) ────────
        # Creates the classic "sweeping" ECG monitor effect
        self.cursor_curve = pg.PlotDataItem(
            pen=pg.mkPen(color=ECG_COLORS['cursor'], width=CURSOR_ERASE_SAMPLES * 0.8),
            skipFiniteCheck=True,
        )
        self.pw.addItem(self.cursor_curve)

        # ── Lead-off overlay ──────────────────────────────────────────────────
        self.lead_off_text = pg.TextItem(
            text='LEAD OFF',
            color=ECG_COLORS['lead_off_txt'],
            anchor=(0.5, 0.5)
        )
        self.lead_off_text.setFont(
            pg.QtGui.QFont('Courier New', 14, pg.QtGui.QFont.Weight.Bold)
        )
        self.lead_off_text.setPos(display_samples / 2, 0)
        self.lead_off_text.setVisible(False)
        self.pw.addItem(self.lead_off_text)

    def _draw_grid(self):
        """Draw standard ECG grid: major lines every 1mV / 200ms."""
        y_range = self.DISPLAY_MV_RANGE * self.ADC_SCALE

        # Horizontal grid lines (amplitude): every 1 mV
        for mv in np.arange(-self.DISPLAY_MV_RANGE, self.DISPLAY_MV_RANGE + 0.1, 1.0):
            y = mv * self.ADC_SCALE
            line = pg.InfiniteLine(
                pos=y,
                angle=0,
                pen=pg.mkPen(
                    color='#003300' if mv != 0 else '#006600',
                    width=1 if mv != 0 else 2
                )
            )
            self.pw.addItem(line)

        # Vertical grid lines (time): every 200ms = 100 samples at 500 Hz
        samples_per_200ms = int(0.2 * self.fs)
        for s in range(0, self.display_samples, samples_per_200ms):
            line = pg.InfiniteLine(
                pos=s,
                angle=90,
                pen=pg.mkPen(color='#003300', width=1)
            )
            self.pw.addItem(line)

    def render(self, display_array: np.ndarray, is_lead_off: bool):
        """
        Update the waveform display with new data.
        Called by render timer — must be FAST (no computation here).

        Args:
            display_array: Pre-processed array from SmoothLeadBuffer.get_display_array()
            is_lead_off: Whether lead is currently disconnected
        """
        if is_lead_off:
            self._show_lead_off()
            return

        self._hide_lead_off()

        n = len(display_array)
        if n == 0:
            return

        # Main waveform
        self.curve.setData(self.x_axis[:n], display_array)

        # Cursor eraser — draw a black stripe at the right edge (write head)
        # This erases old data just ahead of where new data is being written
        # creating the smooth "sweeping" effect of a real ECG monitor
        erase_start = max(0, n - CURSOR_ERASE_SAMPLES)
        erase_x = self.x_axis[erase_start:n]
        # Fill eraser with zeros (baseline) to "wipe" old waveform
        erase_y = np.zeros(len(erase_x))
        self.cursor_curve.setData(erase_x, erase_y)

    def _show_lead_off(self):
        if not self._lead_off_shown:
            self.pw.setBackground(ECG_COLORS['lead_off_bg'])
            self.lead_off_text.setVisible(True)
            self.curve.setData([], [])          # Clear waveform
            self.cursor_curve.setData([], [])
            self._lead_off_shown = True

    def _hide_lead_off(self):
        if self._lead_off_shown:
            self.pw.setBackground(ECG_COLORS['background'])
            self.lead_off_text.setVisible(False)
            self._lead_off_shown = False

    def set_gain(self, mv_range: float):
        """
        Adjust vertical gain (zoom). Standard ECG gains: 5, 10, 20 mm/mV.
        Call from UI gain selector — does NOT cause jitter because Y range
        is only changed when explicitly requested.

        Args:
            mv_range: Half-range in mV (e.g., 2.0 = ±2mV, 5.0 = ±5mV)
        """
        y = mv_range * self.ADC_SCALE
        self.pw.setYRange(-y, y, padding=0)
        # Update label position
        self.label.setPos(10, y * 0.85)
        # Update DISPLAY_MV_RANGE for grid (optional — redraw grid)
        self.DISPLAY_MV_RANGE = mv_range


