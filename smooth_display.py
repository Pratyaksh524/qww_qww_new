def _generate_synthetic_ecg_packet(t: float, sampling_rate: float = 500,
                                    heart_rate: float = 75,
                                    noise_level: float = 5.0) -> dict:
    """
    Generate one synthetic ECG packet (10 samples per lead).
    Used only for the demo — replace with your real serial reader.
    """
    samples_per_packet = 10
    dt = 1.0 / sampling_rate
    adc_center = 2048.0

    packet_data = {}
    for lead in ALL_LEADS:
        samples = []
        for i in range(samples_per_packet):
            t_i = t + i * dt
            rr = 60.0 / heart_rate

            # Synthetic PQRST components
            phase = (t_i % rr) / rr
            ecg = 0.0

            # P wave
            p_center = 0.15
            ecg += 200 * np.exp(-((phase - p_center) ** 2) / (2 * 0.015 ** 2))

            # QRS complex
            q_center = 0.30
            ecg -= 100 * np.exp(-((phase - q_center + 0.02) ** 2) / (2 * 0.003 ** 2))
            ecg += 1000 * np.exp(-((phase - q_center) ** 2) / (2 * 0.004 ** 2))
            ecg -= 150 * np.exp(-((phase - q_center - 0.02) ** 2) / (2 * 0.003 ** 2))

            # T wave
            t_center = 0.55
            ecg += 300 * np.exp(-((phase - t_center) ** 2) / (2 * 0.04 ** 2))

            # Lead-specific scaling
            lead_scale = {'II': 1.0, 'I': 0.8, 'III': 0.6, 'aVR': -0.9,
                         'aVL': 0.3, 'aVF': 0.7, 'V1': -0.5, 'V2': 0.4,
                         'V3': 0.7, 'V4': 0.9, 'V5': 0.8, 'V6': 0.6
                         }.get(lead, 0.7)

            ecg *= lead_scale
            ecg += np.random.normal(0, noise_level)
            samples.append(adc_center + ecg)

        packet_data[lead] = samples
    return packet_data


