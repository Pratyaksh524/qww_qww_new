ALL_LEADS = ['I', 'II', 'III', 'aVR', 'aVL', 'aVF', 'V1', 'V2', 'V3', 'V4', 'V5', 'V6']


class SmoothECGDisplay:
    """
    Complete smooth ECG display manager for all 12 leads.

    Handles:
    - Decoupled data ingestion (any rate) vs render (fixed 20 FPS)
    - Incremental filtering (no full-buffer filtfilt each frame)
    - Fixed Y axes (no auto-range vertical jumping)
    - Scrolling cursor eraser (no wrap-around teleport)
    - Lead-off detection and overlay per lead
    - Thread-safe data push via Qt timer (stays on UI thread)

    USAGE
    -----
        # 1. At app startup (before QApplication):
        configure_pyqtgraph()

        # 2. After creating your plot widgets:
        self.ecg_display = SmoothECGDisplay(
            plot_widgets={'I': self.plot_I, 'II': self.plot_II, ...},
            sampling_rate=500,
            display_seconds=5,
        )

        # 3. In your serial data callback:
        self.ecg_display.push_samples({'I': samples_I, 'II': samples_II, ...})

        # 4. To stop (e.g., on window close):
        self.ecg_display.stop()
    """

    # Render rate: 20 FPS = 50ms. Smooth enough for ECG, not CPU-heavy.
    RENDER_INTERVAL_MS = 50

    def __init__(self,
                 plot_widgets: dict,
                 sampling_rate: float = 500.0,
                 display_seconds: float = 5.0,
                 adc_center: float = 2048.0,
                 adc_scale: float = 204.8,
                 notch_freq: float = 50.0,
                 leads: list = None):
        """
        Args:
            plot_widgets:     Dict mapping lead name → PyQtGraph PlotWidget
                              e.g. {'I': widget_I, 'II': widget_II, ...}
            sampling_rate:    Hardware sampling rate in Hz (default 500)
            display_seconds:  Width of display window in seconds (default 5)
            adc_center:       ADC midpoint value (default 2048 for 12-bit)
            adc_scale:        ADC counts per mV (default 204.8 = ±10mV range)
            notch_freq:       Powerline frequency to notch: 50 (India/EU) or 60 (US)
            leads:            List of lead names to manage. Defaults to all 12.
        """
        self.fs = sampling_rate
        self.display_seconds = display_seconds
        self.display_samples = int(display_seconds * sampling_rate)
        self.adc_center = adc_center
        self.adc_scale = adc_scale
        self.leads = leads or [l for l in ALL_LEADS if l in plot_widgets]

        # ── Per-lead buffers ──────────────────────────────────────────────────
        self.buffers: dict[str, SmoothLeadBuffer] = {}
        for lead in self.leads:
            self.buffers[lead] = SmoothLeadBuffer(
                sampling_rate=sampling_rate,
                display_seconds=display_seconds,
                adc_center=adc_center,
                adc_scale=adc_scale,
                lead_name=lead
            )

        # ── Per-lead renderers ────────────────────────────────────────────────
        self.renderers: dict[str, SmoothLeadRenderer] = {}
        for lead in self.leads:
            if lead in plot_widgets:
                self.renderers[lead] = SmoothLeadRenderer(
                    plot_widget=plot_widgets[lead],
                    lead_name=lead,
                    display_samples=self.display_samples,
                    sampling_rate=sampling_rate
                )

        # ── Render timer (decoupled from data arrival) ────────────────────────
        self._render_timer = QTimer()
        self._render_timer.timeout.connect(self._render_frame)
        self._render_timer.start(self.RENDER_INTERVAL_MS)

        # ── Performance tracking ──────────────────────────────────────────────
        self._frame_count = 0
        self._dropped_frames = 0
        self._last_render_ms = 0

    # ─────────────────────────────────────────────────────────────────────────
    # PUBLIC API
    # ─────────────────────────────────────────────────────────────────────────

    def push_samples(self, lead_data: dict):
        """
        Push new samples from hardware into all lead buffers.
        Call this from your serial/hardware data callback.

        THREAD NOTE: This is safe to call from the UI thread only.
        If your serial reader is on a background thread, use
        push_samples_threadsafe() instead.

        Args:
            lead_data: Dict mapping lead name → list/array of new ADC samples
                       e.g. {'I': [2048, 2052, 2050], 'II': [2100, 2098], ...}
                       Leads not in dict are skipped.
        """
        for lead, samples in lead_data.items():
            if lead in self.buffers and len(samples) > 0:
                self.buffers[lead].push(np.asarray(samples, dtype=float))

    def push_sample_single(self, lead: str, value: float):
        """
        Push a single ADC sample for one lead.
        Convenience method for sample-by-sample ingestion.

        Args:
            lead:  Lead name string e.g. 'II'
            value: Single ADC value (float or int)
        """
        if lead in self.buffers:
            self.buffers[lead].push(np.array([value], dtype=float))

    def push_packet(self, packet: dict):
        """
        Push a single ADC packet (one sample per lead).
        Use when your hardware sends one value per lead per packet.

        Args:
            packet: Dict with one ADC value per lead
                    e.g. {'I': 2048, 'II': 2150, 'III': 2100, ...}
        """
        for lead, value in packet.items():
            if lead in self.buffers:
                self.buffers[lead].push(np.array([float(value)]))

    def set_gain(self, mv_range: float, leads: list = None):
        """
        Set vertical display gain for specified leads (or all leads).

        Args:
            mv_range: Half-range in mV. Standard values:
                      1.0 = ±1mV (2x gain)
                      2.0 = ±2mV (standard)
                      3.0 = ±3mV (default)
                      5.0 = ±5mV (reduced gain)
            leads: List of lead names to apply to. Default: all managed leads.
        """
        target = leads or self.leads
        for lead in target:
            if lead in self.renderers:
                self.renderers[lead].set_gain(mv_range)

    def reset_lead(self, lead: str):
        """
        Reset a single lead buffer and filter state.
        Call when a lead reconnects after being disconnected.

        Args:
            lead: Lead name e.g. 'V1'
        """
        if lead in self.buffers:
            self.buffers[lead].reset()

    def reset_all(self):
        """Reset all lead buffers. Call on restart or new patient."""
        for buf in self.buffers.values():
            buf.reset()

    def get_lead_off_status(self) -> dict:
        """
        Returns lead-off status for all leads.

        Returns:
            Dict mapping lead name → bool (True = disconnected)
            e.g. {'I': False, 'II': False, 'V1': True, ...}
        """
        return {lead: buf.is_lead_off for lead, buf in self.buffers.items()}

    def stop(self):
        """Stop the render timer. Call on window close."""
        self._render_timer.stop()

    def start(self):
        """Restart the render timer after stop()."""
        self._render_timer.start(self.RENDER_INTERVAL_MS)

    def get_performance_stats(self) -> dict:
        """
        Returns rendering performance statistics.
        Useful for debugging jitter / performance issues.

        Returns:
            Dict with frame_count, dropped_frames, last_render_ms
        """
        return {
            'frame_count': self._frame_count,
            'dropped_frames': self._dropped_frames,
            'last_render_ms': self._last_render_ms,
            'render_fps': 1000.0 / max(1, self._last_render_ms)
        }

    # ─────────────────────────────────────────────────────────────────────────
    # INTERNAL RENDER LOOP
    # ─────────────────────────────────────────────────────────────────────────

    def _render_frame(self):
        """
        Called by render timer at fixed 20 FPS.
        Reads from all buffers and calls renderer.render().
        No computation happens here — only buffer reads and setData() calls.
        """
        import time
        t0 = time.perf_counter()

        for lead in self.leads:
            if lead not in self.renderers:
                continue
            buf = self.buffers[lead]
            renderer = self.renderers[lead]

            display_data = buf.get_display_array()
            renderer.render(display_data, buf.is_lead_off)

        self._frame_count += 1
        elapsed_ms = (time.perf_counter() - t0) * 1000
        self._last_render_ms = elapsed_ms

        # Warn if render took longer than the interval (indicates dropped frames)
        if elapsed_ms > self.RENDER_INTERVAL_MS * 0.9:
            self._dropped_frames += 1


